#include<stdio.h>
/**
 * main- entry point
 *
 * Return: 0 success
 */

int main(void)
{
	char alpha_lowercase;

for (alpha_lowercase = 'a'; alpha_lowercase <= 'z'; alpha_lowercase++)
{
	if (alpha_lowercase = 'e')
		continue;


	if (alpha_lowercase = 'q')
		continue;

	putchar(alpha_lowercase);
}

putchar('\n');

return (0);
}



